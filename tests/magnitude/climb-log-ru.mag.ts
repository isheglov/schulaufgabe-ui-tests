import { test } from 'magnitude-test';

test('can add and complete todos', { url: 'https://isheglov.github.io/climb-log/' }, async ({ ai }) => {
    await ai.step('нажми на поле даты и удали все из поля ввода');
    // Очисти и введи дату в формате dd.mm.yyyy
    await ai.step('очисти поле даты, введи "12.01.2024" и нажми Enter');
    await ai.step('прокрути список уровней до "10" (только 10, не 10- и не 10+)');
    // Убедись, что выбран именно уровень "10" (не "10+" и не "10-")
    await ai.step('нажми на кнопку с надписью ровно "10", не "10+" и не "10-"');
    await ai.step('выбери цвет синий');
    await ai.step('нажми "Log Climb"');
    await ai.check('должен отображаться залогированный подъём с уровнем 10 и синим цветом');
    await ai.step('перейди на вкладку "история"');
    await ai.check('должен отображаться подъём уровня 10 синего цвета от 12 января 2024');
});
